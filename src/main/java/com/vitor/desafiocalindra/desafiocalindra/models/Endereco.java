package com.vitor.desafiocalindra.desafiocalindra.models;

public class Endereco {

    public String enderecoInput;
    public Double latitude;
    public Double longitude;

    public String getEnderecoInput() {
        return enderecoInput;
    }

    public void setEnderecoInput(String enderecoInput) {
        this.enderecoInput = enderecoInput;
    }

    public Double getLatitude() {
        return latitude;
    }

    public void setLatitude(Double latitude) {
        this.latitude = latitude;
    }

    public Double getLongitude() {
        return longitude;
    }

    public void setLongitude(Double longitude) {
        this.longitude = longitude;
    }
}
